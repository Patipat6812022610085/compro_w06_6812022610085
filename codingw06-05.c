#include <stdio.h>

int main() {
// ประกาศตัวแปร
int A, B, C, D, E, F, G;

// คำนวณแต่ละนิพจน์
A = -2 + 5 * 2; // 1. 5*2=10, 2. -2+10=8
B = 10/2 * 3; // 1. 10/2=5, 2. 5*3=15
C = 6 / 2 + 3 * (4 % 2); // 1. 4%2=0, 2. 6/2=3, 3. 3*0=0, 4. 3+0=3
D = (5+2) * 15 % 4; // 1. 5+2=7, 2. 7*15=105, 3. 105%4=1
E = 6 + 2 * 2 - 6 / 2; // 1. 2*2=4, 2. 6/2=3, 3. 6+4=10, 4. 10-3=7
F = 5 + 3 * 2 - 8 / 4 + (6 % 5); // 1. 3*2=6, 2. 8/4=2, 3. 6%5=1, 4. 5+6-2+1=10
G = (4 + 3) * 2 - 10 / (2 + 3); // 1. 4+3=7, 2. 2+3=5, 3. 7*2=14, 4. 10/5=2, 5. 14-2=12

// แสดงผลลัพธ์
printf("A = -2 + 5 * 2 = %d\n", A);
printf("B = 10/2 * 3 = %d\n", B);
printf("C = 6 / 2 + 3 * (4 %% 2) = %d\n", C);
printf("D = (5+2) * 15 %% 4 = %d\n", D);
printf("E = 6 + 2 * 2 - 6 / 2 = %d\n", E);
printf("F = 5 + 3 * 2 - 8 / 4 + (6 %% 5) = %d\n", F);
printf("G = (4 + 3) * 2 - 10 / (2 + 3) = %d\n", G);

return 0;
}